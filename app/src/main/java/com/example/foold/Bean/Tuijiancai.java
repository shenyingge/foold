package com.example.foold.Bean;

public class Tuijiancai {

    private int id;
    private String name;
    private int suan;
    private int tian;
    private int la;
    private int dan;
    private int zongfen;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSuan() {
        return suan;
    }

    public void setSuan(int suan) {
        this.suan = suan;
    }

    public int getTian() {
        return tian;
    }

    public void setTian(int tian) {
        this.tian = tian;
    }

    public int getLa() {
        return la;
    }

    public void setLa(int la) {
        this.la = la;
    }

    public int getDan() {
        return dan;
    }

    public void setDan(int dan) {
        this.dan = dan;
    }

    public int getZongfen() {
        return zongfen;
    }

    public void setZongfen(int zongfen) {
        this.zongfen = zongfen;
    }
}
